package ai.logistic;

import ai.model.Student;
import ai.util.FileUtil;

import java.util.List;
import java.util.Scanner;

public class LogisticRegressor {
	List<Student> samples;
	double w1=0, w2=0, b=0;
	
	public LogisticRegressor(List<Student> samples) {
		this.samples = samples;
	}
	
	private double sigmoid(double z) {
		return (1/(1+Math.exp(-z)));
	}
	
	public void train(double learningRate, int epochs) {
		int n = samples.size();
		for (int i=0; i<epochs; i++) {
			double sumE1 = 0;
			double sumE2 = 0;
			double sumE = 0;
			for (Student s: samples) {
				double x1 = s.getScore1();
				double x2 = s.getScore2();
				double p = sigmoid(w1*x1 + w2*x2 + b);
				double err = p - s.getAdmitted();
				
				sumE1 += err*x1;
				sumE2 += err*x2;
				sumE += err;
			}
			double w1_gradient = (1/n) * sumE1;
			double w2_gradient = (1/n) * sumE2;
			double b_gradient = (1/n) * sumE;
			
			w1 = w1 - learningRate * w1_gradient;
			w2 = w2 - learningRate * w2_gradient;
			b = b -  learningRate * b_gradient;
		}
		//test
		System.out.println("训练完成: w1=" + w1 + ", w2=" + w2 + ", b=" + b);
	}
	
	public int predict(double x1, double x2) {
		double p = predictProbability(x1, x2);
		return (p>0.5 ? 1 : 0);
	}
	
	private double predictProbability(double x1, double x2) {
		double z = w1*x1 + w2*x2 + b;
		return sigmoid(z);
	}
	
	public static void main(String[] args) {
		List<Student> samples = FileUtil.loadStudents("data/admission.txt");
		
		LogisticRegressor lr = new LogisticRegressor(samples);
		
		lr.train(0.01, 1000);
		
		System.out.println("成绩(45,67) → " + lr.predict(45, 67));
	    System.out.println("成绩(78,82) → " + lr.predict(78, 82));
	    
	    @SuppressWarnings("resource")
		Scanner sc = new Scanner(System.in);
	    System.out.print("输入两门成绩：");
	    double x1 = sc.nextDouble();
	    double x2 = sc.nextDouble();
	    System.out.println("预测结果：" + lr.predict(x1, x2));
	}
}
